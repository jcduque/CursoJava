package com.javaOpenBoot.conceptos;

public class TiposDatos {
    public static void main(String[] args) {
        //enteros
        byte number = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3 = 3; // 4 byte
        long number4 = 4L; // 8 byte

        //punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        //caracter
        char caracter1 = 'a';

        //booleanos
        boolean verdadero = true;
        boolean falso = false;

        //cadenas de texto
        String nombre = "Jean";
        String apellido = "Duque";

        //tipos envoltorio
        Integer numero = null;
        Long numero2 = 2L;

    }
}
